// 圖片管線產出段：合併「店實拍 pick」（優先，kind:photo）與「菜色圖對映」
// （兜底，kind:dish），產出 src/data/extras/images.ts。
// 用法：node scripts/build-image-data.mjs           — 產檔
//       node scripts/build-image-data.mjs --verify  — 只 HEAD 檢查現有 images.ts 的
//         全部 src 是否還活著（Commons 檔案會被改名/刪除）
import { writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { root, readJson, sleep, UA, loadRestaurants } from './lib/commons.mjs'

const scratch = join(root, 'scratch')
const outPath = join(root, 'src/data/extras/images.ts')

if (process.argv.includes('--verify')) {
  const { execFileSync } = await import('node:child_process')
  const { mkdtempSync, rmSync } = await import('node:fs')
  const { tmpdir } = await import('node:os')
  const { pathToFileURL } = await import('node:url')
  const tmp = mkdtempSync(join(tmpdir(), 'twfood-imgv-'))
  const bundle = join(tmp, 'img.mjs')
  execFileSync(join(root, 'node_modules/.bin/rolldown'), [outPath, '--format', 'esm', '--platform', 'node', '--file', bundle], { stdio: ['ignore', 'ignore', 'inherit'] })
  const { STORE_IMAGES } = await import(pathToFileURL(bundle).href)
  rmSync(tmp, { recursive: true, force: true })
  let dead = 0
  for (const [id, img] of Object.entries(STORE_IMAGES)) {
    const res = await fetch(img.src, { method: 'HEAD', headers: { 'User-Agent': UA } }).catch(() => null)
    if (!res?.ok) {
      dead++
      console.log(`💀 ${id}: ${res?.status ?? 'fetch failed'} ${img.src}`)
    }
    await sleep(200)
  }
  console.log(dead ? `\n${dead} 條失效，重跑管線補圖` : '\n✅ 全部 hotlink 存活')
  process.exit(dead ? 1 : 0)
}

const storeCandidates = readJson(join(scratch, 'store-image-candidates.json'), {})
const storePicks = readJson(join(scratch, 'store-image-picks.json'), {})
const dishCandidates = readJson(join(scratch, 'dish-image-candidates.json'), {})
const dishPicks = readJson(join(scratch, 'dish-image-picks.json'), {})
const pool = readJson(join(root, 'scripts', 'dish-pool.json'), [])
const overrides = readJson(join(scratch, 'dish-map-overrides.json'), {})

const { ALL } = await loadRestaurants()

const dishSlugFor = (r) => {
  if (overrides[r.id]) return overrides[r.id]
  for (const m of r.mustOrder)
    for (const d of pool)
      if ((d.matchKeywords ?? []).some((k) => m.includes(k))) return d.slug
  return `cuisine-${r.cuisine}`
}

const entryOf = (c, kind) => ({
  src: c.src, author: c.author, license: c.license,
  licenseUrl: c.licenseUrl, sourceUrl: c.sourceUrl, kind,
})

const out = {}
let photo = 0, dish = 0
const missing = []
for (const r of ALL) {
  const pi = storePicks[r.id]
  if (Number.isInteger(pi)) {
    const c = storeCandidates[r.id]?.[pi]
    if (!c) {
      console.error(`❌ ${r.id}: store pick index ${pi} 不存在於候選`)
      process.exit(1)
    }
    out[r.id] = entryOf(c, 'photo')
    photo++
    continue
  }
  const slug = dishSlugFor(r)
  const di = dishPicks[slug]
  const c = Number.isInteger(di) ? dishCandidates[slug]?.[di] : undefined
  if (!c) {
    missing.push(`${r.id} → ${slug}`)
    continue
  }
  out[r.id] = entryOf(c, 'dish')
  dish++
}

if (missing.length) {
  console.error(`❌ ${missing.length} 家沒有可用圖（slug 缺候選或缺 pick）：`)
  for (const m of missing.slice(0, 20)) console.error(`  ${m}`)
  process.exit(1)
}

const lines = [
  '// GENERATED by scripts/build-image-data.mjs — 勿手改。',
  '// 來源：Wikimedia Commons（授權白名單 CC0/PD/Attribution/CC BY/CC BY-SA，詳情頁必須署名）。',
  '// 只能被 dynamic import（src/lib/images.ts）——靜態 import 會把整包拖進主 chunk。',
  'import type { StoreImage } from "../types";',
  '',
  'export const STORE_IMAGES: Record<string, StoreImage> = {',
  ...Object.entries(out).map(([id, v]) => `  ${JSON.stringify(id)}: ${JSON.stringify(v)},`),
  '};',
  '',
]
writeFileSync(outPath, lines.join('\n'))
console.log(`✅ ${outPath}\n實拍 ${photo} 家｜菜色示意 ${dish} 家｜合計 ${photo + dish}/${ALL.length}`)
console.log('接著跑 npm run check（圖片規則）與 npm run build')
